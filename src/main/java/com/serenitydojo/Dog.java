package com.serenitydojo;

public class Dog {


    public String name;
    public String favtoy;
    public int age;

    public Dog(String name, String favtoy, int age) {
        this.name = name;
        this.favtoy = favtoy;
        this.age = age;
    }



    public String getname() {
        return name;
    }


    public String getFavtoy() {
        return favtoy;
    }


    public int getAge() {
        return age;
    }
}

 

    